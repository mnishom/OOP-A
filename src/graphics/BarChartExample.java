    private void ChartInit() {
        // Create dataset
        Map<String, Integer> datapenjualan = new HashMap<>();
        datapenjualan.put("Januari", 120);
        datapenjualan.put("Februari", 150);
        datapenjualan.put("Maret", 125);
        datapenjualan.put("April", 110);
        datapenjualan.put("Mei", 160);
        datapenjualan.put("Juni", 180);
        datapenjualan.put("Juli", 110);
        datapenjualan.put("Agustus", 100);
        datapenjualan.put("September", 12);
        datapenjualan.put("Oktober", 20);
        datapenjualan.put("November", 25);
        datapenjualan.put("Desember", 110);

        DefaultCategoryDataset dataset = new DefaultCategoryDataset();

        dataset.addValue(datapenjualan.get("Januari"), "Series1", "Januari");
        dataset.addValue(datapenjualan.get("Februari"), "Series1", "Februari");
        dataset.addValue(datapenjualan.get("Maret"), "Series1", "Maret");
        dataset.addValue(datapenjualan.get("April"), "Series1", "April");
        dataset.addValue(datapenjualan.get("Mei"), "Series1", "Mei");
//
        // Create bar chart
        JFreeChart chart = ChartFactory.createBarChart(
                "Grafik Penjualan 4 Bulan Terakhir", // Chart title
                "Data Penjualan", // X-axis Label
                "Jumlah Penjualan", // Y-axis Label
                dataset // Dataset
        );

        // Display the chart in a panel
        ChartPanel chartPanel = new ChartPanel(chart);
        chartPanel.setPreferredSize(new java.awt.Dimension(800, 600));
        getContentPane().add(chartPanel);
        
        
        
    }

