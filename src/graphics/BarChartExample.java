    private String konversiBulan(int i) {
        String[] monthNames = {
            "Januari", "Februari", "Maret", "April", "Mei", "Juni",
            "Juli", "Agustus", "September", "Oktober", "November", "December"
        };

        String monthName;
        if (i >= 1 && i <= 12) {
            monthName = monthNames[i - 1];
        } else {
            monthName = "unknown";
        }
        return monthName;
    }
